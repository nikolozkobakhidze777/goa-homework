// html-ის სტრუქტურაში ჩაამატეთ მეორე დონის სათაური და წამოიღეთ ის js-ში. შემოიღეთ ერთი ცვლადი და თავიდან გაუტოლეთ 0-ს. როდესაც მეორე დონის სათაურზე მოხდება mouseout მოვლენა, ცვლადი გაზარდეთ 1-ით და მისი მნიშვნელობა დაბეჭდეთ კონსოლში.

let counter = 0;

let h2 = document.getElementById('h2');

h2.onmouseout = function() {
    counter++;
    console.log("mouseout მოხდა", counter);
};
